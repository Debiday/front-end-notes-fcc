// TODO: Sum All Numbers in a Range (sort, for loop)
// ---------------------------------------------------
function sumAll(arr) {
    let finalSum = 0;
    let sorted = arr.sort(function(a,b) {
      return a === b ? 0: a < b ? -1: 1;
    });
    for(let i=sorted[0]; i <= sorted[1]; i++) {
      finalSum += i;
    } return finalSum;
  }
  
  console.log(sumAll([1, 4]));
  console.log(sumAll([10, 5]));
  

